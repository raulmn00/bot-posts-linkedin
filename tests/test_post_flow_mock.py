"""Testes do PostFlowService cobrindo o fluxo principal (com fakes do PostGenerator/Replicate/GCS).

Cada teste roda o flow completo via handle_command + wait_pending — o spawn
está em background. Counts esperados no happy path:
  sent_messages: [0] insights, [1] post bilíngue com botões
  sent_photos:   [0] foto com caption truncado em PT
"""

from typing import NamedTuple

import pytest

from bot_posts_linkedin.config import get_settings
from bot_posts_linkedin.domain.rejection_cause import RejectionCause
from bot_posts_linkedin.domain.states import PostStatus
from bot_posts_linkedin.services.post_flow import PostFlowService
from bot_posts_linkedin.store.chat_state_memory import InMemoryChatStateStore
from bot_posts_linkedin.store.memory import InMemoryPostStore
from tests.fakes import (
    FakeAnthropicClient,
    FakeGcsImageStorage,
    FakeGithubSearchService,
    FakeLinkedInPublisher,
    FakePostGenerator,
    FakeReplicateImageService,
    FakeTelegramClient,
)

CHAT_ID = "999"


class _Bundle(NamedTuple):
    service: PostFlowService
    telegram: FakeTelegramClient
    posts: InMemoryPostStore
    chats: InMemoryChatStateStore
    anthropic: FakeAnthropicClient
    github: FakeGithubSearchService
    post_gen: FakePostGenerator
    replicate: FakeReplicateImageService
    gcs: FakeGcsImageStorage
    linkedin: FakeLinkedInPublisher


def _make_service() -> _Bundle:
    telegram = FakeTelegramClient()
    posts = InMemoryPostStore()
    chats = InMemoryChatStateStore()
    anthropic = FakeAnthropicClient()
    github = FakeGithubSearchService()
    post_gen = FakePostGenerator()
    replicate = FakeReplicateImageService()
    gcs = FakeGcsImageStorage()
    linkedin = FakeLinkedInPublisher(dry_run=False)
    service = PostFlowService(
        post_store=posts,
        chat_state_store=chats,
        telegram_client=telegram,
        anthropic_client=anthropic,
        github_search=github,
        post_generator=post_gen,
        replicate_image=replicate,
        gcs_image=gcs,
        linkedin_publisher=linkedin,
        settings=get_settings(),
    )
    return _Bundle(
        service, telegram, posts, chats, anthropic, github, post_gen, replicate, gcs, linkedin
    )


async def _get_awaiting_post(posts: InMemoryPostStore):
    awaiting = await posts.list_by_status(PostStatus.AWAITING_APPROVAL)
    assert len(awaiting) == 1, f"esperado 1 post em AWAITING_APPROVAL, achei {len(awaiting)}"
    return awaiting[0]


@pytest.mark.asyncio
async def test_happy_path_command_para_aprovacao() -> None:
    b = _make_service()
    b.post_gen.set_body_response(
        "Post em português gerado pelo fake.",
        "Post in English generated by the fake.",
    )

    await b.service.handle_command(CHAT_ID, "teste de assunto", use_github=False)
    await b.service.wait_pending()

    # Insights + post bilíngue
    assert len(b.telegram.sent_messages) == 2
    insights, post_msg = b.telegram.sent_messages
    assert "Insumos coletados" in insights["text"]
    assert "🇧🇷" in post_msg["text"] and "🇺🇸" in post_msg["text"]
    assert "Post em português" in post_msg["text"]
    assert "Post in English" in post_msg["text"]
    assert post_msg["reply_markup"] is not None

    # Photo enviada com caption truncado do PT
    assert len(b.telegram.sent_photos) == 1
    photo = b.telegram.sent_photos[0]
    assert photo["photo"].startswith("https://storage.googleapis.com/")
    assert "Post em português" in photo["caption"]

    post = await _get_awaiting_post(b.posts)
    assert post.user_prompt == "teste de assunto"
    assert post.body_pt and post.body_en
    assert post.image_url is not None
    assert post.image_gcs_path is not None


@pytest.mark.asyncio
async def test_aprovar_publica_e_transiciona_para_published() -> None:
    """Após Fase F: handle_approval dispara publicação em background → PUBLISHED.

    Fluxo: AWAITING_APPROVAL → APPROVED (síncrono, fecha janela de idempotência)
    → spawn _run_publish → PUBLISHED.
    """
    b = _make_service()
    await b.service.handle_command(CHAT_ID, "x", use_github=False)
    await b.service.wait_pending()
    post = await _get_awaiting_post(b.posts)

    await b.service.handle_approval(CHAT_ID, post.id, "cb1")
    await b.service.wait_pending()

    published = await b.posts.list_by_status(PostStatus.PUBLISHED)
    assert len(published) == 1
    assert published[0].linkedin_post_urn == "urn:li:share:7000000000000000000"
    # 2 edits: PUBLISHING_FOOTER (no clique) + footer com link (após publicar).
    assert len(b.telegram.edited_messages) == 2
    assert "publicando" in b.telegram.edited_messages[0]["text"].lower()
    assert "publicado" in b.telegram.edited_messages[1]["text"].lower()
    assert "https://www.linkedin.com/feed/update/" in b.telegram.edited_messages[1]["text"]
    assert any(c["id"] == "cb1" for c in b.telegram.answered_callbacks)
    assert len(b.linkedin.publish_calls) == 1


