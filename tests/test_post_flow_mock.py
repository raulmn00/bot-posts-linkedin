@pytest.mark.asyncio
async def test_motivo_dispara_regeneracao_sem_repesquisar() -> None:
    b = _make_service()
    await b.service.handle_command(CHAT_ID, "x", use_github=True)
    await b.service.wait_pending()
    post = await _get_awaiting_post(b.posts)
    await b.service.handle_rejection(CHAT_ID, post.id, "cb")
    research_count_antes = len(b.anthropic.research_calls)
    github_count_antes = len(b.github.search_calls)
    body_calls_antes = len(b.post_gen.body_calls)
    photos_antes = len(b.telegram.sent_photos)

    await b.service.handle_free_text(CHAT_ID, "muito formal")
    await b.service.wait_pending()

    # Revisão NÃO refaz pesquisa nem GitHub (reaproveita).
    assert len(b.anthropic.research_calls) == research_count_antes
    assert len(b.github.search_calls) == github_count_antes
    # Mas DISPARA novo body + image (decisão MVP: regenera tudo).
    assert len(b.post_gen.body_calls) == body_calls_antes + 1
    assert len(b.telegram.sent_photos) == photos_antes + 1
    # Header de revisão na mensagem do post.
    ultimo_post_msg = b.telegram.sent_messages[-1]
    assert "Revisão #1" in ultimo_post_msg["text"]
    assert "muito formal" in ultimo_post_msg["text"]

    novo = await _get_awaiting_post(b.posts)
    assert novo.id == post.id
    assert novo.revision_count == 1
    assert "muito formal" in novo.revision_feedback


