@pytest.mark.asyncio
async def test_novo_comando_com_pendente_em_revising_pergunta_descartar() -> None:
    b = _make_service()
    await b.service.handle_command(CHAT_ID, "primeiro", False)
    await b.service.wait_pending()
    p1 = await _get_awaiting_post(b.posts)
    await b.service.handle_rejection(CHAT_ID, p1.id, "cb1")

